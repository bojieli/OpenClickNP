bool emitHlsCpp(const be::Build& build, const std::string& outdir,
                DiagnosticEngine& d) {
    namespace fs = std::filesystem;
    fs::path kdir = fs::path(outdir) / "kernels";
    fs::create_directories(kdir);
    bool ok = true;
    for (const auto& k : build.kernels) {
        fs::path p   = kdir / (k.name + ".cpp");
        fs::path tbp = kdir / (k.name + "_tb.cpp");
        std::ofstream f(p);
        if (!f) { d.error({}, "cannot write " + p.string()); ok = false; continue; }
        emitKernel(f, build, k);
        std::ofstream tb(tbp);
        if (tb) emitTestbench(tb, build, k);
    }

    // Also emit a CMakeLists.txt that the build scripts can use to discover
    // the per-kernel sources.
    std::ofstream cm(fs::path(outdir) / "kernels" / "CMakeLists.txt");
    if (cm) {
        cm << "# Auto-generated kernel manifest\n";
        for (const auto& k : build.kernels) {
            cm << "openclicknp_register_kernel(" << k.name << ")\n";
        }
    }
    return ok;
}

