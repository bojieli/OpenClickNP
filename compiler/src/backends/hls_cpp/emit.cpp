// For each kernel, emits:
//   generated/kernels/<name>.cpp     -- HLS top function
//   generated/kernels/<name>_tb.cpp  -- C/RTL co-simulation testbench stub
//
// The kernel skeleton is:
//
//   void NAME(hls::stream<flit_t> &in_1, ..., hls::stream<flit_t> &out_1, ...,
//             [s_axilite signal regs if @]) {
//     #pragma HLS INTERFACE axis port=in_*
//     #pragma HLS INTERFACE axis port=out_*
//     #pragma HLS INTERFACE ap_ctrl_none port=return  // if autorun
//     static <state>
//     <init body, guarded by static bool>
//     while (1) {
//       #pragma HLS PIPELINE II=1
//       <prologue: read input ports / signal>
//       <handler body | signal body>
//       <epilogue: write output ports>
//     }
//   }

#include "openclicknp/passes.hpp"
